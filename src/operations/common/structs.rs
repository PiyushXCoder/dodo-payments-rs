use serde::{Deserialize, Serialize};

#[serde_with::skip_serializing_none]
#[derive(Debug, Serialize, Deserialize, Clone)]
pub struct BillingAddress {
    pub street: String,
    pub city: String,
    pub state: String,
    pub country: String,
    pub zipcode: String,
}

